import 'package:grow_first/features/listing/domain/entities/user.dart';

class UserModel extends User {
  const UserModel({
    required super.id,
    required super.refCode,
    super.refBy,
    required super.name,
    super.userName,
    required super.gender,
    super.dateOfBirth,
    required super.country,
    required super.state,
    required super.city,
    required super.postCode,
    required super.email,
    required super.phone,
    required super.address,
    required super.subLocality,
    super.image,
    required super.balance,
    required super.companyName,
    super.companyAddress,
    super.gstin,
    super.adhar,
    required super.pan,
    super.domain,
    super.codeId,
    super.teamType,
    super.jobTitle,
    super.customerTypeId,
    required super.isBlock,
    required super.role,
    required super.status,
    required super.otpCreatedAt,
    required super.step,
    super.description,
    super.facebookUrl,
    super.instagramUrl,
    super.twitterUrl,
    super.whatsappNumber,
    super.youtubeUrl,
    super.linkedinUrl,
    super.serviceId,
    required super.createdAt,
    required super.updatedAt,
    
  });

  factory UserModel.fromJson(Map<String, dynamic> json) {
    return UserModel(
      id: json['id'],
      refCode: json['ref_code'],
      refBy: json['ref_by'],
      name: json['name'],
      userName: json['user_name'],
      gender: json['gender'],
      dateOfBirth: json['date_of_birth'],
      country: json['country'],
      state: json['state'],
      city: json['city'],
      postCode: json['post_code'],
      email: json['email'],
      phone: json['phone'],
      address: json['address'],
      subLocality: json['sub_locality'],
      image: json['image'],
      balance: json['balance'],
      companyName: json['company_name'],
      companyAddress: json['company_address'],
      gstin: json['gstin'],
      adhar: json['adhar'],
      pan: json['pan'],
      domain: json['domain'],
      codeId: json['codeid'],
      teamType: json['team_type'],
      jobTitle: json['job_title'],
      customerTypeId: json['customer_type_id'],
      isBlock: json['is_block'],
      role: json['role'],
      status: json['status'],
      otpCreatedAt: json['otp_created_at'],
      step: json['step'],
      description: json['description'],
      facebookUrl: json['facebook_url'],
      instagramUrl: json['instagram_url'],
      twitterUrl: json['twitter_url'],
      whatsappNumber: json['whatsapp_number'],
      youtubeUrl: json['youtube_url'],
      linkedinUrl: json['linkedin_url'],
      serviceId: json['service_id'],
      createdAt: DateTime.parse(json['created_at']),
      updatedAt: DateTime.parse(json['updated_at']),
    );
  }
}
