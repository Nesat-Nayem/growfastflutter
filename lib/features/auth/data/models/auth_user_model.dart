import 'package:grow_first/features/auth/domain/entities/auth_user.dart';

class AuthUserModel extends AuthUser {
  const AuthUserModel({
    required super.id,
    required super.refCode,
    super.refBy,
    required super.name,
    super.userName,
    super.gender,
    super.dateOfBirth,
    super.country,
    super.state,
    super.city,
    super.postCode,
    required super.email,
    required super.phone,
    super.address,
    super.subLocality,
    super.image,
    required super.balance,
    super.companyName,
    super.companyAddress,
    super.gstin,
    super.adhar,
    super.pan,
    super.domain,
    super.codeId,
    super.teamType,
    super.jobTitle,
    super.customerTypeId,
    required super.isBlock,
    required super.role,
    required super.status,
    required super.otpCreatedAt,
    required super.step,
    required super.otp,
    required super.createdAt,
    required super.updatedAt,
    super.description,
    super.facebookUrl,
    super.instagramUrl,
    super.twitterUrl,
    super.whatsappNumber,
    super.youtubeUrl,
    super.linkedinUrl,
    super.serviceId,
  });

  factory AuthUserModel.fromJson(Map<String, dynamic> json) {
    return AuthUserModel(
      id: json['id'],
      refCode: json['ref_code'],
      refBy: json['ref_by'],
      name: json['name'],
      userName: json['user_name'],
      gender: json['gender'],
      dateOfBirth: json['date_of_birth'],
      country: json['country'],
      state: json['state'],
      city: json['city'],
      postCode: json['post_code'],
      email: json['email'],
      phone: json['phone'],
      address: json['address'],
      subLocality: json['sub_locality'],
      image: json['image'],
      balance: json['balance'] ?? 0,
      companyName: json['company_name'],
      companyAddress: json['company_address'],
      gstin: json['gstin'],
      adhar: json['adhar'],
      pan: json['pan'],
      domain: json['domain'],
      codeId: json['codeid'],
      teamType: json['team_type'],
      jobTitle: json['job_title'],
      customerTypeId: json['customer_type_id'],
      isBlock: json['is_block'],
      role: json['role'],
      status: json['status'],
      otpCreatedAt: json['otp_created_at'],
      step: json['step'],
      otp: json['otp'],
      createdAt: DateTime.parse(json['created_at']),
      updatedAt: DateTime.parse(json['updated_at']),
      description: json['description'],
      facebookUrl: json['facebook_url'],
      instagramUrl: json['instagram_url'],
      twitterUrl: json['twitter_url'],
      whatsappNumber: json['whatsapp_number'],
      youtubeUrl: json['youtube_url'],
      linkedinUrl: json['linkedin_url'],
      serviceId: json['service_id'],
    );
  }

  // create toJson method
  Map<String, dynamic> toJson() {
    return {
      'id': id,
      'ref_code': refCode,
      'ref_by': refBy,
      'name': name,
      'user_name': userName,
      'gender': gender,
      'date_of_birth': dateOfBirth,
      'country': country,
      'state': state,
      'city': city,
      'post_code': postCode,
      'email': email,
      'phone': phone,
      'address': address,
      'sub_locality': subLocality,
      'image': image,
      'balance': balance,
      'company_name': companyName,
      'company_address': companyAddress,
      'gstin': gstin,
      'adhar': adhar,
      'pan': pan,
      'domain': domain,
      'codeid': codeId,
      'team_type': teamType,
      'job_title': jobTitle,
      'customer_type_id': customerTypeId,
      'is_block': isBlock,
      'role': role,
      'status': status,
      'otp_created_at': otpCreatedAt,
      'step': step,
      'otp': otp,
      'created_at': createdAt.toIso8601String(),
      'updated_at': updatedAt.toIso8601String(),
      'description': description,
      'facebook_url': facebookUrl,
      'instagram_url': instagramUrl,
      'twitter_url': twitterUrl,
      'whatsapp_number': whatsappNumber,
      'youtube_url': youtubeUrl,
      'linkedin_url': linkedinUrl,
      'service_id': serviceId,
    };
  }
}
