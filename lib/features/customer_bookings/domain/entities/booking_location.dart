class BookingLocation {
  final int id;
  final String name;
  final String address;
  final int staffCount;
  final int country;
  final int state;
  final int city;

  BookingLocation({
    required this.id,
    required this.name,
    required this.address,
    required this.staffCount,
    required this.country,
    required this.state,
    required this.city,
  });
}
